<h2>Wie funktioniert colour.name</h2>

<p>Dieses Langzeitprojekt untersucht die Namen der Farben nach Geschlecht, Ausgabmedium, Zeitpunkt und Ort.
Zunächst wollte ich einige Farbnamen vorgeben, damit der Proband auf dem Farbkreis die entsprechende Farbe
anwählen kann. Dieses «Auswahl nach Name»-Verfahren hat den Vorteil, dass die Abstufung zu ähnlichen Farben
von den Probanden präziser vorgenommen würde. Der Nachteil, dass ich bei den Vorgaben niemals alle Namen
aufzeigen könnte, liegt auf der Hand.</p>

<h3>Freie Namenswahl</h3>
<p>Wir haben uns entschieden, eine beliebige Farbe aus dem Farbwürfel mit den Kanten <span style="color: #F00;">ROT</span>,
  <span style="color: #0F0;">GRÜN</span> und <span style="color: #00F;">BLAU</span> durch die Probanden bennenen zu lassen.
  So sind auch Farben wie «Erdbeergrün» (Die Farbe der Erdbeerblätter) möglich.</p>

<h3>Erfassung</h3>
<p>Erfasst werden pro «Nomination» die Farbe (RGB), der Name, die Sprache, der Netzhauttyp (im Wesentlichen weiblich oder männlich), 
   die Uhrzeit und die IP-Adresse. Die IP-Adresse ist nicht von belang, kann aber evtl. verwendet werden, um die Region zu evaluieren.
Sie wird zu keinem anderen Zweck eingesetzt.
Mit diesen Angaben ist es auch möglich, herauszufinden, ob sich Farbnamen (wie z. B. «Pink») in der Zeit oder nach Region
variieren.</p>

<h3>Gelb</h3>
<p>Unsere Augen sind in den Bereichen Grün und Rot ziemlich empfindlich. Dies führt dazu, dass wir sehr viele Gelb-Varianten
  gut unterscheiden können. Diese sind in einem RGB-Farbrad aber in einem sehr kleinen Bereich zu finden.
  Ich habe daher bei der zufälligen Auswahl das Farbrad im Gelb-Bereich etwas gespreizt, damit die Gelbtöne auch
  etwas häufiger auftreten.</p>

<h3>Auswertungen</h3>
<p>Die Auswertungen suchen im Wesentlichen nur nach Namen, die mehr als drei mal genannt wurden. 
Andere Nennungen werden in einigen Statistiken ignoriert.</p>

