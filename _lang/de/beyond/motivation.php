<h2>Warum entstand diese Seite?</h2>

<img src="<?php echo $TPL_PATHS->getClientRoot(); ?>/img/kleider.jpg" title="Rosarot" alt="Wäsche am Trockner" style="border: 0;"/>

<p>Rosarot, Magenta, Lila, Pink und Himbeere haben etwas gemeinsam:
Sie werden synonym gebraucht, oft verwechselt und ab und zu auch um zu
verallgemeinern in den falschen Topf geworfen.

Dabei sind so viele Namen vorhanden, dass oft eigentlich eine viel
genauere Abstimmung möglich wäre. Andererseits gibt es einge Bereiche
im Farbspektrum, die im Deutschen als Weisse-Flecken auf dem Farbkreis
betitelt werden könnten. Hier heißt es: <a href="<?php echo $TPL_PATHS->getClientRoot(); ?>/nominate">kreativ werden</a>!</p>

<p>Auf der Skipiste in Valbella beim Studium von dutzenden (z. B. rosaroten?) Skijacken ist dann die Entscheidung gefallen:
Klarheit und eine genaue Farbabstufung muss her!
Dank den Babykleidern meiner Tochter weiß ich nun auch, dass gerade im Lila-Rosarot-Pink-Fuchsia-Magenta Bereich jede 
nur erdenkliche Kombination und Mischung möglich ist.</p>


<p>Alle, die Spaß an Farben und deren Namen haben, sind hiermit
aufgefordert, den 16.7 Millionen Farben, die mit den heutigen 
Computerbildschirmen möglich sind, präzise Namen zu <a href="<?php echo $TPL_PATHS->getClientRoot(); ?>/nominate">vergeben</a>, 
beziehungsweise ihre eigene Farbwahrnehmung zu testen und zu
schulen.</p>

<p>Aber <b>Achtung</b>: Die Monitore zeigen nur einen kleinen Ausschnitt aus der Farbenpracht der 
Natur. Zudem variieren die Farben stark von Hersteller zu Hersteller und von 
Modell zu Modell. Bekannt ist auch, dass Frauen und Männer eine 
andere Farbwahrnehmung haben. Geben Sie also beim Mitspielen unbedingt 
den Monitortypen und Ihr Geschlecht an.</p>

<h2>Nutzen</h2>
<p>Sprachliche Bezeichnungen unterliegen regionalen,
geschlechtsspezifischen und Ausgabemedium abhängigen teils minimalen,
teils größeren Unterschieden. Sich diesen Nuancen nicht nur bewusst zu sein,
sondern diese auch quantifizieren zu können,
kann für jede der folgenden Zielgruppen den einen oder anderen Nutzen bringen.</p>

