<h2>How does «colour.name» work?</h2>

<p>«colour.name» is a long time survey to explore names of colours depending on sex, medium, timestamp and place.
  First, I wanted to give a fix list of some names and ask the internet community what is the RGB-Value. So I
  wanted to program a colour-wheel. It would have given a more precise answer to «what is pink».
  On the other hand, I would never have used all the thousands of names of colours, which are around.</p>

<h3>Free selection of names</h3>
<p>So, we decided to choose a random colour out of the rgb-cube using a random value for each of the colours 
  <span style="color: #F00;">RED</span>,
  <span style="color: #0F0;">GREEN</span> and <span style="color: #00F;">BLUE</span>. Every subject (propositus) has to 
  «invent» its own names. So, names like «strawberry green» (colour of strawberry leafs) are possible.</p>

<h3>Survey</h3>
<p>We register for each nomination the colour (RGB), the name, its language,
  the type of the retina (female, male, colour blind), timestamp and ip-address.
  The IP-Address has no matter of moment; it could be used to find out the region, to which the naming belongs
  (the IP-address is not used for any other purpose).
  Using these attributes, it can be possible to assign a name of a colour to a given sex, time and/or region.</p>


<h3>Yellow</h3>
<p>Our eyes are very sensible for green and red. So our eyes are also very sensitive to yellow
  (as addition of green and red).
  Unfortunately «yellow» makes only a very small angle in the rgb-colour wheel.
  So I stretched the wheel in the yellow part at bit, that there are more random positions in the «yellow» region.</p>

<h3>Analysis</h3>
<p>The statistics only show names, that have been mentioned more than 3 times. All other colour-names are
ignored by the «nearest colour statistics».</p>

